package modelo;

public class Criterio {

    private String codCri;
    private String nomCri;
    private String pesCri;
    private String estCri;

    public String getCodCri() {
        return codCri;
    }

    public void setCodCri(String codCri) {
        this.codCri = codCri;
    }

    public String getNomCri() {
        return nomCri;
    }

    public void setNomCri(String nomCri) {
        this.nomCri = nomCri;
    }

    public String getPesCri() {
        return pesCri;
    }

    public void setPesCri(String pesCri) {
        this.pesCri = pesCri;
    }

    public String getEstCri() {
        return estCri;
    }

    public void setEstCri(String estCri) {
        this.estCri = estCri;
    }
    
    

}
