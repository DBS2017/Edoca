package modelo;

public class Aula {

    private String codAul;
    private String secAul;
    private String graAul;
    private String codtut;
    private String estAul; //estado del aula
    private String autTut;

    public String getAutTut() {
        return autTut;
    }

    public void setAutTut(String autTut) {
        this.autTut = autTut;
    }

    public String getCodAul() {
        return codAul;
    }

    public void setCodAul(String codAul) {
        this.codAul = codAul;
    }
        
   

    public String getSecAul() {
        return secAul;
    }

    public void setSecAul(String secAul) {
        this.secAul = secAul;
    }

    public String getGraAul() {
        return graAul;
    }

    public void setGraAul(String graAul) {
        this.graAul = graAul;
    }

    public String getEstAul() {
        return estAul;
    }

    public void setEstAul(String estAul) {
        this.estAul = estAul;
    }

    public String getCodtut() {
        return codtut;
    }

    public void setCodtut(String codtut) {
        this.codtut = codtut;
    }

}
