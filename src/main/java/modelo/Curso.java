package modelo;

public class Curso {
    private int CODCUR;
    private String NOMCUR;
    private String ESTCUR;

    public int getCODCUR() {
        return CODCUR;
    }

    public void setCODCUR(int CODCUR) {
        this.CODCUR = CODCUR;
    }

    public String getNOMCUR() {
        return NOMCUR;
    }

    public void setNOMCUR(String NOMCUR) {
        this.NOMCUR = NOMCUR;
    }

    public String getESTCUR() {
        return ESTCUR;
    }

    public void setESTCUR(String ESTCUR) {
        this.ESTCUR = ESTCUR;
    }

    
}
